let _currentLang = 'en'
const _listeners = new Set()

export function setLang(lang) {
  _currentLang = lang in STRINGS ? lang : 'en'
  _listeners.forEach((cb) => cb(_currentLang))
}
export function getLang() { return _currentLang }

/** React hook: returns t(key) for the current language; re-renders on change. */
import { useSyncExternalStore } from 'react'
export function useT() {
  const lang = useSyncExternalStore(
    (cb) => { _listeners.add(cb); return () => _listeners.delete(cb) },
    () => _currentLang,
    () => 'en',
  )
  return (key, fallback) => {
    const pack = STRINGS[lang] || STRINGS.en
    return pack[key] ?? STRINGS.en[key] ?? fallback ?? key
  }
}
